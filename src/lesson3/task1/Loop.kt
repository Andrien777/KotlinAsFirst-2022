fun hasDifferentDigits(n: Int): Boolean {
    var answer = 0
    for (i in 0..9) {
        if (digitCountInNumber(n, i) > 0)
            answer++
    }
    return answer > 1
}

/**
 * Средняя (4 балла)
 *
 * Для заданного x рассчитать с заданной точностью eps
 * sin(x) = x - x^3 / 3! + x^5 / 5! - x^7 / 7! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю.
 * Подумайте, как добиться более быстрой сходимости ряда при больших значениях x.
 * Использовать kotlin.math.sin и другие стандартные реализации функции синуса в этой задаче запрещается.
 */
fun sin(x: Double, eps: Double): Double {
    val closeX = x % (PI * 2)
    var term = closeX
    var answer = 0.0
    var i = 2
    while (abs(term) > eps) {
        answer += term
        term = (-1.0).pow(i - 1) * closeX.pow(2 * i - 1) / factorial(2 * i - 1)
        i++
    }
    return answer
}

/**
 * Средняя (4 балла)
 *
 * Для заданного x рассчитать с заданной точностью eps
 * cos(x) = 1 - x^2 / 2! + x^4 / 4! - x^6 / 6! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 * Подумайте, как добиться более быстрой сходимости ряда при больших значениях x.
 * Использовать kotlin.math.cos и другие стандартные реализации функции косинуса в этой задаче запрещается.
 */
fun cos(x: Double, eps: Double): Double {
    val closeX = x % (PI * 2)
    var term = 1.0
    var answer = 0.0
    var i = 1
    while (abs(term) > eps) {
        answer += term
        term = (-1.0).pow(i) * closeX.pow(2 * i) / factorial(2 * i)
        i++
    }
    return answer
}

/**
 * Сложная (4 балла)
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var lastDigitNumber = 0
    if (n == 0)
        return 0
    var square = 0
    var i = 0
    while (lastDigitNumber < n) {
        i++
        square = i * i
        lastDigitNumber += digitNumber(square)
    }
    val delta = lastDigitNumber - n
    for (j in 0 until delta) square /= 10
    return square % 10
}

/**
 * Сложная (5 баллов)
 *
 * Найти n-ю цифру последовательности из чисел Фибоначчи (см. функцию fib выше):
 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun fibSequenceDigit(n: Int): Int {
    var lastDigitNumber = 0
    if (n == 0)
        return 0
    var fibNumber = 0
    var i = 0
    while (lastDigitNumber < n) {
        i++
        fibNumber = fib(i)
        lastDigitNumber += digitNumber(fibNumber)
    }
    val delta = lastDigitNumber - n
    for (j in 0 until delta) fibNumber /= 10
    return fibNumber % 10
}
